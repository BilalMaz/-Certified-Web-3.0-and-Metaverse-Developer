// Store the person's name in a variable using var
var personName: string = "Bilal Mazhar ";

// Convert the name to lowercase and store it in a variable
var lowercaseName: string = personName.toLowerCase();

// Convert the name to uppercase and store it in a variable
var uppercaseName: string = personName.toUpperCase();


// Print the names
console.log("Original Name: " + personName);
console.log("Lowercase Name: " + lowercaseName);
console.log("Uppercase Name: " + uppercaseName);
